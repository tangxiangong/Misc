# ╔═╡ 717aaee0-c53d-41df-b793-cc20c1c362e5
md"""
### Solver
Suppose that we are now at the ``n``--th step, and let ``\vec{a}`` denote the solution at last step, that is, ``\vec{u}_h^{n-1}``. Then, the bilinear form corresponding to the convection term is 
```math
\mathrm{conv}((\vec{u}, p), (\vec{v}, q)) = ((\vec{a}\cdot\nabla)\vec{u}, \vec{v}).
```
And the linear functional will be
```math
\ell((\vec{v}, q)) = \tau_n(\vec{f}(t_n), \vec{v}) + (\vec{a}, \vec{v}).
```
"""

# ╔═╡ c8c894bb-8d40-47db-bb3f-bd5c3a97b401
function solver(temporalgrids, fes, u⃗⁰, f⃗)
    t = temporalmesh(temporalgrids...)
    τ = diff(t)
    (X, Y), (Uₕ, _), (__, dΩₕ) = fes
    M, S = assemble_mass_stiff_matrix(X, Y, dΩₕ)
    A = similar(M)
    C = similar(M)
    num_dofs = num_free_dofs(X)
    b = zeros(num_dofs)
    solution = zeros(num_dofs)
    u⃗ⁿ = u⃗⁰
    @inbounds for n in eachindex(τ)
        conv((u⃗, p), (v⃗, q)) = ∫(v⃗ ⊙ ((∇(u⃗))' ⋅ u⃗ⁿ))dΩₕ
        C .= assemble_matrix(conv, X, Y)
        A .= M + τ[n] * (S + C)
        f⃗ⁿ(x) = τ[n] * f⃗(x, t[n+1])
        ℓ((v⃗, q)) = ∫(f⃗ⁿ ⋅ v⃗ + u⃗ⁿ ⋅ v⃗)dΩₕ
        b .= assemble_vector(ℓ, Y)
        cg!(solution, A, b)
        u⃗ⁿ, ___ = FEFunction(X, solution)
    end
    u⃗ⁿ
end

