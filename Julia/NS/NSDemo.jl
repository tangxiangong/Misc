# ╔═╡ 2f1d880a-98b4-45b5-bf54-58ee7e23d41c
md"""
### Assembly Matrix
Next, we will assmebly the mass and stiff matrix corresponding to our discretization.
First of all, let me introduce the bilinear form used in the matrix assembly.

The mass bilinear form is
```math
\text{mass}((\vec{u}, p), (\vec{v}, q)) = (\vec{u}, \vec{v}),
```
and the stiff bilinear form is
```math
\text{stiff}((\vec{u}, p), (\vec{v}, q)) = (\nabla\vec{u}, \nabla\vec{v}) - (p, \nabla\cdot\vec{v}) + (\nabla\cdot\vec{u}, q).
```
"""

# ╔═╡ 518a1fa5-e788-4f86-8ece-5cabafafa2cd
function assemble_mass_stiff_matrix(X, Y, dΩₕ)
    mass((u⃗, p), (v⃗, q)) = ∫(u⃗ ⋅ v⃗)dΩₕ
    stiff((u⃗, p), (v⃗, q)) = ∫(∇(u⃗) ⊙ ∇(v⃗) - p * (∇ ⋅ v⃗) + (∇ ⋅ u⃗) * q)dΩₕ
    M = assemble_matrix(mass, X, Y)
    S = assemble_matrix(stiff, X, Y)
    M, S
end

