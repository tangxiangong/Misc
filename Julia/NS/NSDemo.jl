# ╔═╡ dae8e4cf-4d20-427c-9d27-79debc8c2447
md"""
The incompressible Navier--Stokes equation with homogenous Dirichlet boundary values on a convex smooth/polygonal bounded domain ``\Omega\subseteq\mathbb{R}^2`` reads
```math
\begin{cases}
\partial_t \vec{u}(t) + (\vec{u}(t)\cdot∇)\vec{u}(t) - \Delta\vec{u}(t) + \nabla p(t) = \vec{f}(t), & \text{ in } \Omega \times (0, T], \\
\nabla\cdot\vec{u}(t) = 0, & \text{ in } \Omega\times (0,T], \\
\vec{u}(0) = \vec{u}^0, & \text{ in } \Omega, \\
\vec{u} = \vec{0}, & \text{ on } \partial\Omega,
\end{cases}
```
where the initial value ``\vec{u}^0`` and the source term ``\vec{f}`` are given functions.

The **weak form** corresponding to the above N--S equation is: find ``(\vec{u}(t), p(t))\in \vec{U}\times P`` for every ``t\in (0,T]``, such that for any ``(\vec{v}, q)\in \vec{U}\times P``, there exists 
```math
\begin{cases}
(\partial_t\vec{u}(t),\vec{v}) + ((\vec{u}(t)\cdot\nabla)\vec{u}(t),\vec{v}) + (\nabla\vec{u}(t),\nabla\vec{v}) - (p(t), \nabla\cdot\vec{v}) = (\vec{f}(t),\vec{v}), & \\
(\nabla\cdot\vec{u}(t), q) = 0, \\
\vec{u}(0) = \vec{u}^0.
\end{cases}
```
Here, we use a unified notation ``(\cdot, \cdot)`` to represent both the (scalar) ``L^2(\Omega)`` inner product and the (vector) ``(L^2(\Omega))^2``  version, the Hilbert spaces
```math
\vec{U} = (H_0^1(\Omega))^2,\quad P = \left\{f\in L^2(\Omega):\int_{\Omega}f = 0 \right\}.
```

Suppose that we already have the trial finie element spaces ``X_h = \vec{U}_h\times P_h\subseteq \vec{U}\times P``, which consists of the inf--sup conditions, and denote the test function spaces by ``Y_h = \vec{V}_h\times Q_h`` (in the conforming case, ``X_h = Y_h``). Then, the **finite element scheme** for the N--S equation is: find ``(\vec{u}_h(t), p_h(t)) \in X_h`` for ``t\in (0,T]``, such that for any test function ``(\vec{v}_h, q_h)\in Y_h`` , there holds
```math
\begin{cases}
(\partial_t\vec{u}_h(t),\vec{v}_h) + ((\vec{u}_h(t)\cdot\nabla)\vec{u}(t),\vec{v}_h) + (\nabla\vec{u}_h(t),\nabla\vec{v}_h) - (p_h(t), \nabla\cdot\vec{v}_h) = (\vec{f}(t),\vec{v}_h), & \\
(\nabla\cdot\vec{u}_h(t), q_h) = 0, \\
\vec{u}_h(0) = P_h\vec{u}^0.
\end{cases}
```

Let ``0=t_0<t_1<\cdots<t_{n-1}<t_N=T`` be a qusiuniform partition of the interval ``[0, T]``, with time--step ``\tau_n = t_k-t_{k-1}`` for ``n=1,\,\cdots, \,N``. The semi--implicit Euler--FEM fully discretization is of the form 
```math
\begin{cases}
\left(\dfrac{\vec{u}_h^n-\vec{u}_h^{n-1}}{\tau_n},\vec{v}_h\right) + ((\vec{u}_h^{n-1}\cdot\nabla)\vec{u}^n,\vec{v}_h) + (\nabla\vec{u}_h^n,\nabla\vec{v}_h) - (p_h^n, \nabla\cdot\vec{v}_h) = (\vec{f}(t_n),\vec{v}_h), & \\
(\nabla\cdot\vec{u}_h^n, q_h) = 0.
\end{cases}
```

"""

