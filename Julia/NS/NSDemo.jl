function norm_error(u₁, u₂, dΩ)
    e = u₁ - u₂
    L²_error = ∫(e ⋅ e)dΩ |> sum |> sqrt
    H¹_error = ∫(e ⋅ e + ∇(e) ⊙ ∇(e))dΩ |> sum |> sqrt
    L²_error, H¹_error
end

